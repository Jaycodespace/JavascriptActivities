// 1. Function greet
function greet(name) {
    return "Hello, " + name;
}
let greeting = greet("Ryan");
console.log(greeting);

// 2. Function printMessage
function printMessage(message) {
    return message;
}
let message1 = printMessage("First Message");
let message2 = printMessage("Second Message");
let message3 = printMessage("Third Message");

console.log(message1);
console.log(message2);
console.log(message3);

// 3. Function getSquare
function getSquare(num) {
    return num * num;
}
let squareResult = getSquare(2);
console.log(squareResult);

// 4. Function double
function double(num) {
    return num * 2;
}
let doubledValue = double(2);
console.log(doubledValue);

// 5. Function addTen
function addTen(num) {
    return num + 10;
}
let addedValue = addTen(2);
console.log(addedValue);

